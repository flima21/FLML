    private void btnExcluiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluiActionPerformed
        mysqldb.conectaBanco();
        boolean status = false;
        
        try {
            if (txtLogin.getText().isBlank()) {
                JOptionPane.showMessageDialog(rootPane, "O campo LOGIN não pode ser vazio para realizar a exclusão");
            }
            else {
                String query = "DELETE FROM sistema_usuario WHERE nm_login = '" + txtLogin.getText() + "'";
                
                status = this.mysqldb.updateSQL(query);
                
                if (status) {
                    JOptionPane.showMessageDialog(rootPane, "Usuário excluído com sucesso!");
                    limpaForm();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Usuário não pôde ser excluído");
                }
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        } finally {
            mysqldb.fechaBanco();
        }
    }//GEN-LAST:event_btnExcluiActionPerformed

    private void btnAtualizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizaActionPerformed
                try {
            mysqldb.conectaBanco();

            Usuario usuario = new Usuario();
            
            usuario.setLogin(txtLogin.getText());
            usuario.setNome(txtNome.getText());
            
            String pass = new String(txtSenha.getPassword());
            
            String query = 
                     "UPDATE sistema_usuario set "
                    +"nm_usuario = '" + usuario.getNome()+ "',"
                    +"nm_login   = '" + usuario.getLogin()+ "'";
            
            if (!pass.isEmpty()) {
                usuario.setSenha(usuario.setCriptografia(pass));
                
                query = query + ", senha = '" + usuario.getSenha() + "' ";
            }
            
            query = query + "WHERE nm_login = '" + usuario.getLogin() + "'";
            
            int status = 0;

            status = this.mysqldb.insertSQL(query);

            if (status == 1) {
                JOptionPane.showMessageDialog(rootPane, "Usuário atualizado com sucesso!");
                limpaForm();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Houve problemas na atualização do Usuário. Revise os dados.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "ERRO: Não foi possível atualizar o registro.");
        } finally {
            mysqldb.fechaBanco();
        }
    }//GEN-LAST:event_btnAtualizaActionPerformed
    
    private void limpaForm(){
        txtLogin.setText(null);
        txtSenha.setText(null);
        txtNome.setText(null);
        
        txtLogin.setEnabled(true);
    }
    
