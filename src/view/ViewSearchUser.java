    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        try {
            Usuario novo_usuario = new Usuario();
            
            mysqldb.conectaBanco();
            
            if(txtLogin.getText().isBlank()){
                JOptionPane.showMessageDialog(rootPane, "ERRO: O campo LOGIN não pode ser vazio para efetuar a busca");
            }
            else {
                String query = 
                        "select * "
                       +"from sistema_usuario "
                       +"where nm_login = '"+txtLogin.getText()+"'";
                
                this.mysqldb.executarSQL(query);
                
                while (this.mysqldb.getResultSet().next()) {
                    novo_usuario.setLogin(this.mysqldb.getResultSet().getString(2));
                    novo_usuario.setSenha(this.mysqldb.getResultSet().getString(3));
                    novo_usuario.setNome(this.mysqldb.getResultSet().getString(4));
                    novo_usuario.setLogin(this.mysqldb.getResultSet().getString(5));
                }
                
                if (!novo_usuario.getLogin().isEmpty()){
                    txtLogin.setEnabled(false);
                    
                    txtNome.setText(novo_usuario.getNome());
                }
                else {
                    JOptionPane.showMessageDialog(rootPane, "O usuário procurado não existe na base de dados.");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "ERRO: Não foi possível buscar o registro.");
        } finally {
            mysqldb.fechaBanco();
        }
    }//GEN-LAST:event_btnBuscaActionPerformed

