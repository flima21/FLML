    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        mysqldb.conectaBanco();
        
        Funcionario novo_funcionario = new Funcionario();
        
        try {
            if (docCliente.getText().isBlank()) {
                JOptionPane.showMessageDialog(rootPane, "O campo DOCUMENTO não pode ser vazio para realizar a busca");
            } else {
                String query
                        = "select * "
                        + "from cad_funcionario "
                        + "where nr_cpf_cnpj = '" + docCliente.getText()+"'";
                System.out.println(query);
                this.mysqldb.executarSQL(query);

                while (this.mysqldb.getResultSet().next()) {
                    novo_funcionario.setTpFj(this.mysqldb.getResultSet().getString(2));
                    novo_funcionario.setNrCpfCnpj(this.mysqldb.getResultSet().getString(3));
                    novo_funcionario.setNmCliente(this.mysqldb.getResultSet().getString(4));
                    
                    novo_funcionario.setFgAtivo(Integer.parseInt(this.mysqldb.getResultSet().getString(5)));
                    
                    novo_funcionario.setNmEmail(this.mysqldb.getResultSet().getString(6));
                    novo_funcionario.setNrCep(this.mysqldb.getResultSet().getString(7));
                    novo_funcionario.setDsEndereco(this.mysqldb.getResultSet().getString(8));
                    novo_funcionario.setSgEstado(this.mysqldb.getResultSet().getString(9));
                    novo_funcionario.setNmMunicipio(this.mysqldb.getResultSet().getString(10));
                }

                if (!novo_funcionario.getNrCpfCnpj().isEmpty()) {
                    
                    txtNameFuncionario.setText(novo_funcionario.getNmCliente());
                    txtEmailFuncionario.setText(novo_funcionario.getNmEmail());
                    cbMunicipio.setSelectedItem(novo_funcionario.getNmMunicipio());
                    txtCep.setText(novo_funcionario.getNrCep());
                    txtLogradouro.setText(novo_funcionario.getDsEndereco());
                    cbEstado.setSelectedItem(novo_funcionario.getSgEstado());

                    if (novo_funcionario.getFgAtivo()== 1) {
                        cbFgAtivo.setSelectedItem("Sim");
                    } else {
                        cbFgAtivo.setSelectedItem("Não");
                    }

                    docCliente.setEnabled(false);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "O cliente procurado não existe na base de dados.");
                }

            }
        }
        catch(Exception e){
           JOptionPane.showMessageDialog(rootPane, "ERRO: Cliente não encontrado"); 
        }
        finally{
            mysqldb.fechaBanco();
        }
           

    }//GEN-LAST:event_btnBuscaActionPerformed

