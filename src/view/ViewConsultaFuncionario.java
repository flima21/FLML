    private void btnExcluiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluiActionPerformed
        mysqldb.conectaBanco();
        boolean status = false;
        
        try {
            if (docCliente.getText().isBlank()) {
                JOptionPane.showMessageDialog(rootPane, "O campo DOCUMENTO não pode ser vazio para realizar a exclusão");
            }
            else {
                String query = "DELETE FROM cad_funcionario WHERE nr_cpf_cnpj = '" + docCliente.getText() + "'";
                
                status = this.mysqldb.updateSQL(query);
                
                if (status) {
                    JOptionPane.showMessageDialog(rootPane, "Funcionário excluído com sucesso!");
                    limpaForm();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Funcionário não pôde ser excluído");
                }
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        } finally {
            mysqldb.fechaBanco();
        }
    }//GEN-LAST:event_btnExcluiActionPerformed

