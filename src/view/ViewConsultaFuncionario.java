    private void btnAtualizabtnCadastra(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizabtnCadastra
        try {
            mysqldb.conectaBanco();

            Funcionario funcionario = new Funcionario();
            
            funcionario.setNrCpfCnpj(format.NrDb(docCliente.getText()));
            funcionario.setNmCliente(txtNameFuncionario.getText());

            if (cbFgAtivo.getSelectedItem().equals("Sim")) {
                funcionario.setFgAtivo(1);
            } else {
                funcionario.setFgAtivo(0);
            }

            funcionario.setNmEmail(txtEmailFuncionario.getText());
            funcionario.setNrCep(format.NrDb(txtCep.getText()));
            funcionario.setDsEndereco(txtLogradouro.getText());
            funcionario.setSgEstado((String)cbEstado.getSelectedItem());
            funcionario.setNmMunicipio((String)cbMunicipio.getSelectedItem());

            String query =
                     "UPDATE cad_funcionario set "
                    +"nr_cpf_cnpj = '" + funcionario.getNrCpfCnpj()  + "',"
                    +"nm_cliente  = '" + funcionario.getNmCliente()  + "',"
                    +"fg_ativo    =  " + funcionario.getFgAtivo()    + ","
                    +"nm_email    = '" + funcionario.getNmEmail()    + "',"
                    +"nr_cep      = '" + funcionario.getNrCep()      + "',"
                    +"ds_endereco = '" + funcionario.getDsEndereco() + "',"
                    +"sg_estado   = '" + funcionario.getSgEstado()   + "',"
                    +"nm_municipio= '" + funcionario.getNmMunicipio()+ "' "
                    +"WHERE nr_cpf_cnpj = '" + funcionario.getNrCpfCnpj() + "'";
            
            int status = 0;

            status = this.mysqldb.insertSQL(query);

            if (status == 1) {
                JOptionPane.showMessageDialog(rootPane, "Cliente atualizado com sucesso!");
                limpaForm();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Houve problemas na atualização do cliente. Revise os dados.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "ERRO: Não foi possível atualizar o registro.");
        } finally {
            mysqldb.fechaBanco();
        }
    }//GEN-LAST:event_btnAtualizabtnCadastra

